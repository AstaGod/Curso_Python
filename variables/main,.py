## Reglas para  nombrar variables
## 1. solo pueden tener los siguientes caracteres
##      - Letras minusculas
##      - Letras mayusculas
##      - Digitos
##      - Guiones bajos (_)
## 2. solo deben iniciar con letras y guion bajo no con digitos
## 3. no pueden ser una palabra reservada del lenguaje

